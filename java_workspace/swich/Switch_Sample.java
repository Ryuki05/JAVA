package java_workspace.swich;

public class Switch_Sample {
    public static void main(String[] args) {
        int num = 3;

        switch (num) {
            case 1:
                System.out.println("1です");
                break;
            case 2:
                System.out.println("2です");
                break;
            case 3:
                System.out.println("3です");
                break;
            case 4:
                System.out.println("4です");
                break;
            default:
                break;
        }
    }
}
